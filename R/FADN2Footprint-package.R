"_PACKAGE"

#' @details
#' \strong{WARNING:} This package has been validated only for cereals
#' (TF14 = 15) and dairy farms (TF14 = 45). Filter your FADN data accordingly
#' before use.
#'
#' FADN2Footprint computes environmental, economic and social footprints from
#' FADN (Farm Accountancy Data Network) data. It supports both national FADN
#' (combined with LPIS and spatial data) and EU FADN as inputs.
#'
#' @section Output indicators:
#' \itemize{
#'   \item \strong{Farm practices:} fertiliser use, pesticide use, tillage
#'     intensity, crop diversity, yields, hedge density, mean field size,
#'     ground cover, livestock density, feed intake, manure management
#'   \item \strong{Environmental:} GHG emissions, biodiversity impact,
#'     water consumption and pollution
#'   \item \strong{Economic:} price premium, added value, operating margin,
#'     labour-to-product ratio
#'   \item \strong{Social:} generational renewal, farmer education
#' }
#'
